import time


def get_time(func):
    def wrapper(*args, **kwargs):
        start = time.time()
        res = func(*args, **kwargs)
        end = time.time()
        print(func.__name__, end - start)
        return res

    return wrapper


@get_time
def init_list():
    lst = []
    for i in range(1000000):
        lst.append(i)
    return lst


@get_time
def init_dict():
    dict = {}
    for i in range(1000000):
        dict[i] = i
    return dict


@get_time
def add_to_list(lst):
    for i in range(len(lst), len(lst) + 1000000):
        lst.append(i)


@get_time
def add_to_dict(dict):
    for i in range(1000000, 2000000):
        dict[i] = i


@get_time
def dell_list(lst):
    for i in range(1000):
        lst.pop(i)


@get_time
def dell_dict(dict):
    for i in range(1000000, 2000000):
        dict.pop(i)
        dict[i] = i


